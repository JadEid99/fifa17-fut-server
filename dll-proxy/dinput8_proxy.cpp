static BYTE* FindPattern(BYTE* start, SIZE_T size, const BYTE* pattern, SIZE_T patternLen) {
    for (SIZE_T i = 0; i <= size - patternLen; i++) {
        if (memcmp(start + i, pattern, patternLen) == 0)
            return start + i;
    }
    return NULL;
}

